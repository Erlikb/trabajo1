# ejercicio 1

num1 = int(input("Ingresa un numero: "))
num2 = int(input("Ingresa el segundo numero: "))

suma = num1 + num2 #operaciones aritmeticas basicas
resta = num1 - num2
multiplicacion = num1 * num2
division = num1 / num2

print("La sumam de los dos numeros es: ",suma,
      "\nLa resta de los dos numeros es: ",resta,
      "\nLa multiplicacion de los dos numeros es: ",multiplicacion,
      "\nLa division de los dos numeros es: ",division) # impresion de los resultados

